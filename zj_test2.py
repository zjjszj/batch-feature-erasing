from torch.autograd import Variable

def get_y_pred(outputs,target):
    outputs=nn.Softmax(dim=1)(outputs).cuda()
    i, j = 0, 0
    y_pred = []
    while j < len(target):
        y_pred.append(outputs[i, target[j]])
        i += 1
        j += 1
    y_pred = torch.tensor(y_pred,requires_grad=True).cuda()
    return y_pred


class Model(nn.Module):
    def __init__(self):
        super(Model,self).__init__()
        self.features=nn.Sequential(
            nn.Conv2d(3,3,3,padding=1),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(kernel_size=2,stride=2)
        )
        self.classifier=nn.Sequential(
            nn.Linear(2*2*3,5)
        )
    def forward(self,x):
        x=self.features(x)
        x=x.view(x.size(0),-1)
        x=self.classifier(x)
        x=nn.Softmax(dim=1)(x)                  #加一个Softmax层
        return x


model=Model().cuda()
model.train()
